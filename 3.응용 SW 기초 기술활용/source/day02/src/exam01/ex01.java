package exam01;

public class ex01 {
             // 자료형, 참조형 기본값
    static int num1;     // 0
    static double num2;   // 0.0
    static boolean bool1; // false
    static String str;    // null

    public static void main(String[] args) {
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(bool1);
        System.out.println(str);

    }
}
